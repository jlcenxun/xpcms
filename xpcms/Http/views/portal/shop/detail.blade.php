						 <p>　　原标题：“乔碧萝殿下”“红花会贝贝” 上主播黑名单</p>
						<p>　　昨天本报报道了红花会成员贝贝在直播中疑似自残并被封号的消息，8月6日，中国演出行业协会网络表演分会发布了《网络表演（直播）行业主播黑名单（第三批）》，“红花会贝贝”名列其中，近日引发网友争议的网络主播“乔碧萝殿下”也一样受到封禁5年的制裁。</p>
						<p>　　<strong>两主播引发网友争议</strong></p>
						<p><strong>　　演出行业协会将其列入黑名单</strong></p>
						<p>　　中国演出行业协会网络表演分会官方微信公众号6日发布了《网络表演（直播）行业主播黑名单（第三批）》，北青报记者注意到，因在直播中容貌发生变化引发网友争议的主播“乔碧萝殿下”和另一因“剁手指”引发网友争议的主播“红花会贝贝”均名列黑名单中。</p>

						<p>　　中国演出行业协会网络表演分会介绍，根据《网络表演（直播）行业主播“黑名单”管理制度（试行）》，被列入黑名单的主播将在行业内禁止注册和直播，封禁期限5年。</p>
						<p>　　中国演出行业协会网络表演分会表示，近期，一些低俗、恶意炒作、有碍公序良俗的违法违规直播事件多次出现，引发社会广泛关注，损害了行业形象。“各直播平台严格自律，积极承	担主体责任，第一时间向分会上报了劣迹主播信息及其违规证据，分会组织内容评议委员会专家组进行审核，并经分会会长联席会讨论通过，正式确定了第三批网络表演（直播）行业主播‘黑名单’。”该协会分会亦表示，主播如对被列入黑名单有争议，可以在20个工作日内向该分会秘书处进行申诉。</p>

						<p>　　<strong>本期黑名单内共59人</strong></p>
						<p><strong>　　“乔碧萝殿下”曾被两直播平台封禁</strong></p>

						<p>　　北青报记者注意到，包括“乔碧萝殿下”和“红花会贝贝”两名近期引发争议的主播在内，此次名列黑名单的网络主播达59个。</p>

						<p>　　“乔碧萝殿下”为斗鱼APEX英雄分区主播，今年6月17日首次直播，直播内容以游戏直播加上少部分的闲聊为主，但在直播时该主播并不露脸，只是以声音和粉丝交流，其声线甜美，主动在斗鱼晒出的“个人美照”也是颜值上佳的少女，由此吸引了一批粉丝的关注。</p>

						<p>　　7月25日，“乔碧萝殿下”与人连麦直播时发生“翻车事件”，平时用来遮挡脸部的图片不见了，还透露自己身份证上显示已经58岁。8月1日凌晨，斗鱼平台发布声明称，因为“乔碧萝殿下”的一场自主炒作，挑战公众底线等不当行为，决定对“乔碧萝殿下”进行永久封停以儆效尤。</p>

						<p>　　几天后，“乔碧萝殿下”再次在Bilibili视频网进行直播。但没过多久，直播间就被该视频网站永久封禁。</p>

						<p>　　据了解，此前中国演出行业协会网络表演（直播）分会曾公布两批黑名单主播，其中包括卢本伟、天佑、牌牌琦等主播。</p>

